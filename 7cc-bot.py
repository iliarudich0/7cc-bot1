    query.answer()
    
    # –ü–æ–ª—É—á–∞–µ–º –∫–ª—é—á –ø—É–Ω–∫—Ç–∞ –º–µ–Ω—é, –ø–æ –∫–æ—Ç–æ—Ä–æ–º—É –Ω–∞–∂–∞–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
